-- POLÍTICAS BÁSICAS (Lectura pública para productos, privada para el resto)
CREATE POLICY "Productos visibles para todos" ON products FOR SELECT USING (true);
CREATE POLICY "Permitir todo a autenticados en products" ON products FOR ALL USING (auth.role() = 'authenticated');
CREATE POLICY "Perfiles editables por el propio usuario" ON profiles FOR ALL USING (auth.uid() = id);

-- POLÍTICAS PARA VENTAS Y PAGOS (Solo usuarios autenticados)
CREATE POLICY "Permitir todo a autenticados en sales" ON sales FOR ALL USING (auth.role() = 'authenticated');
CREATE POLICY "Permitir todo a autenticados en sale_items" ON sale_items FOR ALL USING (auth.role() = 'authenticated');
CREATE POLICY "Permitir todo a autenticados en payments" ON payments FOR ALL USING (auth.role() = 'authenticated');
